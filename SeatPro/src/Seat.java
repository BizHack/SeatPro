
public class Seat {

	private int s_id;
	private int u_id;
	private int rowN;
	private int colN;
	private String state;
	public int getS_id() {
		return s_id;
	}
	
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	public int getRowN() {
		return rowN;
	}
	public void setRowN(int rowN) {
		this.rowN = rowN;
	}
	public int getColN() {
		return colN;
	}
	public void setColN(int colN) {
		this.colN = colN;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	

}
